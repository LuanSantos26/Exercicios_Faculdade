numero=int(input("Digite um número inteiro: "))
for i in range(1, 11):
    resultado = numero * i
    print(f"{numero} x {i} = {resultado}")
# O programa solicita ao usuário um número inteiro e, em seguida, utiliza um loop para calcular e exibir a tabuada desse número de 1 a 10.

contador = 10
while contador > 0:
    print(contador)
    contador -= 1
# O programa utiliza um loop while para contar de 10 até 1, exibindo cada número na tela.
# O loop continua até que o contador chegue a 0, momento em que o loop termina.